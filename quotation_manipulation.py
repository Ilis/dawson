# Quotation manipulations

quote = "I think there is a world market for maybe five computers."

print("\nOrigin:")
print(quote)
print("\nUpper:")
print(quote.upper())
print("\nLower:")
print(quote.lower())
print("\nTitle:")
print(quote.title())
print("\nReplace:")
print(quote.replace("five", "some mils"))
print("\nOrigin:")
print(quote)
